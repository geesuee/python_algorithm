import sys
input = sys.stdin.readline
sys.setrecursionlimit(10**6)

def dfs(graph, start, visited):
    visited[start] = True

    for i in graph[start]:
        if not visited[i]:
            dfs(graph, i, visited)

def solution():
    N, M = map(int, input().split())
    graph = [[] for _ in range(N+1)]

    for _ in range(M):
        v1, v2 = map(int, input().split())
        graph[v1].append(v2)
        graph[v2].append(v1)
    
    group = 0
    visited = [False] * (N+1)
    for v in range(1, N+1):
        if not visited[v]:
            dfs(graph, v, visited)
            group += 1
    
    print(group)

if __name__ == "__main__":
    solution()